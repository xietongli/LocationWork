public class Friend {
    String image;
    String name;
    public Friend(String image,String name){
        this.image=image;
        this.name=name;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage(){
        return image;
    }

}
